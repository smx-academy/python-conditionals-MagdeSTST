""" Напишете програма која ќе прочита два броја од корисникот и ќе го испечати производот на броевите,
    да се провери дали резултатот е парен или не парен."""

x1 = int(input('Внесете го првиот број:'))
x2 = int(input('Внесете го вториот број:'))

p_proizv = x1 * x2

print(f'Производот на броја {x1} и {x2} e {p_proizv}')


p_div2 = p_proizv%2

if p_div2 == 0:
    print(f'Производот на броеви {x1} и {x2}  е парен број')
else:
    print(f'Производот на броеви {x1} и {x2} е непарен број')